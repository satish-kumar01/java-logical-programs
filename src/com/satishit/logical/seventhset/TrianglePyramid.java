package com.satishit.logical.seventhset;

public class TrianglePyramid {
    public static void main(String[] args) {
        for (int i=0;i<5;i++){
            // inner for loop
            for (int j=0;j<=i;j++){
                System.out.print(j+" ");
            }
            System.out.println();
        }
    }
}
