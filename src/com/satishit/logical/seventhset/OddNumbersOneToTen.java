package com.satishit.logical.seventhset;

public class OddNumbersOneToTen {
    public static void main(String[] args) {
        //For printing the odd numbers from 1 to 10
        for (int i =1; i<=10; i++){
            if (i%2==1){
                System.out.println(i);
            }
        }
    }
}
