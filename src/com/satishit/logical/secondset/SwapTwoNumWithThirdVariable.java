package com.satishit.logical.secondset;

public class SwapTwoNumWithThirdVariable {
    public static void main(String[] args) {
        int a = 8;
        int b = 5;
        int temp;
        // Swapping the values in the variables a and b
        temp = a;
        a = b;
        b = temp;
        System.out.println("a is storing: "+a);
        System.out.println("b is storing: "+b);
    }

}
